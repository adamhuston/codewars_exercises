''' Demonstrate the use of a for loop'''

# ask user to enter a sentence

phrase = input('Please enter a phrase: ')

# print each word on a separate line
# phrase is what we are looping through
# eachLetter will have a letter one at a time, as we loop
for letter in phrase:
    print(letter)